            <h2>Hexokinase</h2>
          </div>
          <div class="content">
            <p>Hexokinase performs the first step in glycolysis, using an ATP molecule to start the process. It transfers a phosphate from ATP to glucose, forming glucose-6-phosphate. Daniel Koshland realized, a decade before the structure was known, that this chemical reaction must be shielded from water, to keep the phosphate from being simply cleaved off of ATP by a water molecule. So he proposed that hexokinase performs an induced fit, closing around ATP and glucose once they are bound. When several structures of yeast hexokinase were solved in the 1970's, this was shown to be true. Hexokinase is shaped like a clamp, with a big groove in one side (shown below with an arrow).</p>
            <p>The structure without glucose, shown at bottom left from PDB entry 2yhx, is open, allowing access to the active site. But when glucose binds, as shown at bottom center from PDB entry 1hkg, it closes down, surrounding the molecule. These two structures were solved before the amino acid sequence was known, so the structures are incomplete. For an updated view, take a look at entry 1ig8 for the open form, and entry 1bdg (which is a similar form from a blood fluke) for the glucose-bound form.</p>
